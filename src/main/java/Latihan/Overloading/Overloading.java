package Latihan.Overloading;

public class Overloading {

    // function
    int penjumlahan(int angka, int angka2) {
        int hasil = angka + angka2;
        return  hasil;
    }

    int penjumlahan(int angka, int angka2, int angka3){
        int hasil1 = angka + angka2 + angka3;
        return hasil1;
    }

    int penjumlahan(int angka, int angka2 , int angka3 , int angka4){
        int hitung = angka + angka2 + angka3 + angka4;
        return hitung;
    }
}
